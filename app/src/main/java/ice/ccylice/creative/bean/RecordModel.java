package ice.ccylice.creative.bean;

public class RecordModel {
    private int recordIcon;
    private String recordText;
    private String recordIconName;

    public int getRecordIcon() {
        return recordIcon;
    }

    public void setRecordIcon(int recordIcon) {
        this.recordIcon = recordIcon;
    }

    public String getRecordText() {
        return recordText;
    }

    public void setRecordText(String recordText) {
        this.recordText = recordText;
    }

    public String getRecordIconName() {
        return recordIconName;
    }

    public void setRecordIconName(String recordIconName) {
        this.recordIconName = recordIconName;
    }
}
